package ch.simas.jtoggl.domain;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonRootName;

/**
 * Toggl Project client representation.
 *
 * @author Simon Martinelli
 */
@JsonRootName("client")
@JsonInclude(JsonInclude.Include.NON_NULL)
public class ProjectClient implements Cloneable, WithWorkspace, WithId {

    @JsonProperty("id")
    private Long id;
    @JsonProperty("name")
    private String name;
    @JsonProperty("hrate")
    private String hourlyRate;
    @JsonProperty("cur")
    private String currency;
    private Workspace workspace;
    @JsonProperty("notes")
    private String notes;
    @JsonProperty("wid")
    private Long workspaceId;

    public ProjectClient() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getHourlyRate() {
        return hourlyRate;
    }

    public void setHourlyRate(String hourlyRate) {
        this.hourlyRate = hourlyRate;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    @Override
    public Workspace getWorkspace() {
        return workspace;
    }

    @Override
    public void setWorkspace(Workspace workspace) {
        this.workspace = workspace;
    }


    @Override
    public String toString() {
        return "Client{" + "id=" + id + ", name=" + name + ", hourlyRate=" + hourlyRate + ", currency=" + currency
                + ", notes=" + notes + ", workspace=" + workspace + '}';
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ProjectClient other = (ProjectClient) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 71 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    public Long getWorkspaceId() {
        return workspaceId;
    }

    public void setWorkspaceId(Long workspaceId) {
        this.workspaceId = workspaceId;
    }

    @Override
    public ProjectClient clone() {
        ProjectClient cl = new ProjectClient();
        cl.id = id;
        cl.name = name;
        cl.hourlyRate = hourlyRate;
        cl.currency = currency;
        cl.workspace = workspace;
        cl.notes = notes;
        cl.workspaceId = workspaceId;
        return cl;
    }
}
