package ch.simas.jtoggl.domain;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;
import com.fasterxml.jackson.annotation.JsonRootName;

/**
 * Toggl representation of project / user mapping.
 *
 * @author Simon Martinelli
 */
@JsonRootName("user")
@JsonInclude(JsonInclude.Include.NON_NULL)
public class ProjectUser implements Cloneable, WithWorkspace, WithUser, WithId {

    @JsonProperty("id")
    private Long id;
    @JsonProperty("rate")
    private Double hourlyRate;
    @JsonProperty("manager")
    private Boolean manager;
    private Project project;
    private User user;
    @JsonProperty("pid")
    private Long projectId;
    @JsonProperty("uid")
    private Long userId;
    @JsonProperty("wid")
    private Long workspaceId;
    private Workspace workspace;

    public ProjectUser() {
    }

    public Double getHourlyRate() {
        return hourlyRate;
    }

    public void setHourlyRate(Double hourlyRate) {
        this.hourlyRate = hourlyRate;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Boolean getManager() {
        return manager;
    }

    public void setManager(Boolean manager) {
        this.manager = manager;
    }

    public Project getProject() {
        return project;
    }

    public void setProject(Project project) {
        this.project = project;
    }

    @Override
    public User getUser() {
        return user;
    }

    @Override
    public void setUser(User user) {
        this.user = user;
    }


    @Override
    public String toString() {
        return "ProjectUser{" + "id=" + id + ", hourlyRate=" + hourlyRate + ", manager=" + manager + ", project=" +
                project + ", user=" + user + '}';
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ProjectUser other = (ProjectUser) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 41 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    public Long getProjectId() {
        return projectId;
    }

    public void setProjectId(Long projectId) {
        this.projectId = projectId;
    }

    @Override
    public Long getUserId() {
        return userId;
    }

    @Override
    public void setUserId(Long userId) {
        this.userId = userId;
    }

    @Override
    public ProjectUser clone() {
        ProjectUser pu = new ProjectUser();
        pu.id = id;
        pu.hourlyRate = hourlyRate;
        pu.manager = manager;
        pu.project = project;
        pu.user = user;
        pu.projectId = projectId;
        pu.userId = userId;
        return pu;

    }

    public Long getWorkspaceId() {
        return workspaceId;
    }

    public void setWorkspaceId(Long workspaceId) {
        this.workspaceId = workspaceId;
    }

    @Override
    public Workspace getWorkspace() {
        return workspace;
    }

    @Override
    public void setWorkspace(Workspace workspace) {
        this.workspace = workspace;
    }
}
